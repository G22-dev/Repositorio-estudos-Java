package Poo;

public class NavegadorInternet {

    private String paginaUrl; 
    private String novaAba;
    private String atualizarPagina;
    
    public String getPaginaUrl() {
        return paginaUrl;
    }

    public void setPaginaUrl(String paginaUrl) {
        this.paginaUrl = paginaUrl;
    }

    public String getNovaAba() {
        return novaAba;
    }

    public void setNovaAba(String novaAba) {
        this.novaAba = novaAba;
    }

    public String getAtualizarPagina() {
        return atualizarPagina;
    }

    public void setAtualizarPagina(String atualizarPagina) {
        this.atualizarPagina = atualizarPagina;
    }
   
}
