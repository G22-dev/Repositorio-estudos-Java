package Poo;

public class ReprodutorMusica {

    private String tocar;
    private String pausar;
    private String pulandoMusica;
    
    public String getTocar() {
        return tocar;
    }

    public void setTocar(String tocar) {
        this.tocar = tocar;
    }

    public String getPausar() {
        return pausar;
    }

    public void setPausar(String pausar) {
        this.pausar = pausar;
    }

    public String getTocarMusica() {
        return pulandoMusica;
    }

    public void setTocarMusica(String pulandoMusica) {
        this.pulandoMusica = pulandoMusica;
    }
}
